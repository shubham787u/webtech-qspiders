public class Logical {
    public static void main(String[] args) {
        int a =10;
        int b =20;
        System.out.println(a++>10 && b++>15);
        System.out.println(a++>10 || b++>15);
        System.out.println(a>10 != b>15);
        //System.out.println(++a>10 && b++>15);
        System.out.println(a);
        System.out.println(b);
    }
}
